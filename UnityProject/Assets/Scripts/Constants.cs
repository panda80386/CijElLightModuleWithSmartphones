public class Constants
{

    const byte ON = 0x01;
    const byte OFF = 0x00;

    const byte C = 0x10;
    const byte I = 0x20;
    const byte J = 0x30;

    public const byte AUTO = 0x50;
    public const byte MANUAL = 0x51;

    public const byte C_ON = C | ON;
    public const byte C_OFF = C | OFF;
    public const byte I_ON = I | ON;
    public const byte I_OFF = I | OFF;
    public const byte J_ON = J | ON;
    public const byte J_OFF = J | OFF;

}
